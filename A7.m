clear;
clc;
Vs = 2400;
Req = 0.83;
Xeq = 0.546;
Ps=7500;
Rc= 25;
Ve = 2428.125;
amps = 0:31.25;
a = [0.1, 0.4, 0.75, 1];
b =sqrt(1-a.*a);
Is(1,:)= amps.*(a(1,1)+b(1,1).*j);
Is(2,:)= amps.*(a(1,2)+b(1,2).*j);
Is(3,:)= amps.*(a(1,3)+b(1,3).*j);
Is(4,:)= amps.*(a(1,4)+b(1,4).*j);
Pcu = Req .* amps.*amps;
Pcore = Ve/Rc;
efficiency (1,:) = (Ps.*acos(a(1,1)))./(Pcu+Pcore+Ps.*acos(a(1,1)))*100;
efficiency (2,:) = (Ps.*acos(a(1,2)))./(Pcu+Pcore+Ps.*acos(a(1,2)))*100;
efficiency (3,:) = (Ps.*acos(a(1,3)))./(Pcu+Pcore+Ps.*acos(a(1,3)))*100;
efficiency (4,:) = (Ps.*acos(a(1,4)))./(Pcu+Pcore+Ps.*acos(a(1,4)))*100;
hold on;
plot(amps,efficiency (1,:),'b-');
plot(amps,efficiency (2,:),'r-');
plot(amps,efficiency (3,:),'g-');
plot(amps,efficiency (4,:),'y-');
title ('Efficiency versus Current ');
xlabel('Current');
ylabel('Efficiency (%)');
legend('Power Factor = 0.1','Power Factor = 0.4','Power Factor = 0.75','Power Factor = 1')
hold off;
